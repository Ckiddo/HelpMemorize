from PIL import Image

def itoc(number):
	st = "@ . ."
	sts = st.split(' ')
	return sts[int(number/255.0 * len(sts))]

img = Image.open('/Users/chenkang/Downloads/Ck.jpg')

with open("/Users/chenkang/Downloads/Ckjpg.txt","wb") as charFile:
	w,h = img.size
	img.thumbnail((w//3,h//3))
	img = img.convert("P")
	w,h = img.size
	for x in range(w):
		for y in range(h):
			charFile.write( itoc(img.getpixel((y,x))))
		charFile.write('\n')
	charFile.close()





	